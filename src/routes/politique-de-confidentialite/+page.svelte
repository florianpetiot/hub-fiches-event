  <h1 class="text-2xl font-bold text-text-main">Politique de Confidentialité</h1>
</div>

<div class="md:mx-5 mb-5 text-text-main italic">
    Dernière mise à jour : mai 2026
</div>

<div class="md:mx-5 mb-5 text-text-main/30">
  <p><span class="font-bold">1. Responsable du traitement</span> : L'École Polytechnique Universitaire de Nantes Université, rue Christian Pauc, CS 50609, 44306 Nantes cedex 3, est responsable du traitement des données personnelles collectées via cette plateforme.</p>
</div>

<div class="md:mx-5 mb-5 text-text-main/30">
    <p><span class="font-bold">2. Délégué à la Protection des Données (DPO)</span> : Pour toute question relative à la protection de vos données, vous pouvez contacter le délégué à la protection des données de l'établissement à l'adresse suivante : [DPO@etablissement.fr]</p>
</div>

<div class="md:mx-5 mb-5 text-text-main">
    <p><span class="font-bold">3. Données collectées</span> : Dans le cadre du fonctionnement de la plateforme, les données suivantes sont collectées :</p>
    <ul class="ml-3">
        <li>- nom, adresse email (à la création du compte)</li>
        <li>- rôle au sein de l'établissement</li>
        <li>- contenu des fiches événements soumises (titre, dates, lieu, effectif, description, documents joints)</li>
        <li>- messages échangés dans la messagerie interne</li>
        <li>- horodatages des actions (soumission, signature, validation)</li>
    </ul>
</div>

<div class="md:mx-5 mb-5 text-text-main">
    <p><span class="font-bold">4. Finalités du traitement</span> : Ces données sont collectées pour les finalités suivantes :</p>
    <ul class="ml-3">
        <li>- Gestion et instruction des demandes d'organisation d'événements</li>
        <li>- Workflow de validation et de signature des fiches</li>
        <li>- Communication entre les clubs et les services administratifs de l'établissement</li>
    </ul>
</div>

<div class="md:mx-5 mb-5 text-text-main">
    <p><span class="font-bold">5. Base légale</span> : Le traitement repose sur l'exécution d'une mission d'intérêt public confiée à l'établissement (article 6.1.e du RGPD), dans le cadre de la gestion de la vie associative étudiante.</p>
</div>

<div class="md:mx-5 mb-5 text-text-main">
    <p><span class="font-bold">6. Destinataires des données</span> : Les données sont accessibles aux seuls agents habilités de l'établissement dans le cadre de l'instruction des dossiers, ainsi qu'aux clubs concernés pour leurs propres fiches.</p>
</div>

<div class="md:mx-5 mb-5 text-text-main/30">
    <p><span class="font-bold">7. Durée de conservation</span> : Les fiches événements et les données associées sont conservées pendant une durée maximale d'un an après la date de l'événement, puis supprimées automatiquement. Les comptes utilisateurs sont conservés pendant la durée d'activité du compte au sein de l'établissement.</p>
</div>

<div class="md:mx-5 mb-5 text-text-main/30">
    <p><span class="font-bold">8. Hébergement et transferts</span> : Les données sont hébergées au sein de l'Union Européenne par Supabase Inc. (région Frankfurt, Allemagne). Aucun transfert de données en dehors de l'Union Européenne n'est effectué pour les données personnelles.</p>
</div>

<div class="md:mx-5 mb-5 text-text-main">
    <p><span class="font-bold">9. Vos droits</span> : Conformément au RGPD et à la loi Informatique et Libertés, vous disposez des droits suivants :</p>
    <ul class="ml-3">
        <li>- Droit d'accès : obtenir une copie des données vous concernant</li>
        <li>- Droit de rectification : corriger des données inexactes</li>
        <li>- Droit à l'effacement : demander la suppression de votre compte</li>
        <li>- Droit d'opposition : vous opposer à certains traitements</li>
    </ul>
    <p>Pour exercer ces droits, contactez-nous à : sosinfo@polytech.univ-nantes.fr ou adressez-vous au DPO de l'établissement.</p>
    <p>Vous disposez également du droit d'introduire une réclamation auprès de la CNIL (www.cnil.fr).</p>
</div>

<div class="md:mx-5 mb-5 text-text-main">
    <p><span class="font-bold">10. Cookies</span> : Cette plateforme n'utilise pas de cookies à des fins publicitaires ou de tracking. Un cookie de session est utilisé pour maintenir votre connexion. Il est supprimé à la fermeture de votre session.</p>
</div>